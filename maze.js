let maze = rawMaze.map(row => row.split(""));
const mazeDiv = document.getElementById("maze");
const scoreDiv = document.getElementById("score");
let score = 0;

let player = { x: -1, y: -1 };
let goal = { x: -1, y: -1 };

const positions = [
    [{ x: 1, y: 1 }, { x :89, y :29 }, ],
    [{ x: 89, y: 29 }, { x: 1, y: 1},], 
    [{ x: 89, y: 1}, {x: 1, y:29 } ],
    [{ x: 1, y :29}, {x :89, y:1}],
];

function init() {
    player = positions[score % 4][0];
    goal = positions[score % 4][1];

    maze[player.y][player.x] = "@";
    maze[goal.y][goal.x] = "E";

    scoreDiv.textContent = "score: " + score;

    drawMaze();
}

function drawMaze() {
    const rendered = maze.map(row => row.join("")).join("\n");
    mazeDiv.textContent = rendered;
}

function move(dx, dy) {
    const newX = player.x + dx;
    const newY = player.y + dy;
    const target = maze[newY][newX];

    if (target === "#" ) return; 
    if (target === "E") {
        maze[player.y][player.x] = " ";
        player.x = newX;
        player.y = newY;
        maze[player.y][player.x] = " ";
        drawMaze();
        score++;
        init();
        return;
    }

    maze[player.y][player.x] = " ";
    player.x = newX;
    player.y = newY;
    maze[player.y][player.x] = "@";
    drawMaze();
}

document.addEventListener("keydown", (e) => {
    switch (e.key) {
        case "h": move(-1, 0); break;
        case "l": move(1, 0); break;
        case "k": move(0, -1); break;
        case "j": move(0, 1); break;
    }
});

init();

